/* ══════════════════════════════════════════════════════════════════
   Formspree AJAX submit
   ══════════════════════════════════════════════════════════════════ */
var form      = document.getElementById('contact-form');
var submitBtn = document.getElementById('form-submit-btn');
var statusEl  = document.getElementById('form-status');

form.addEventListener('submit', async function (e) {
    e.preventDefault();

    var t = translations[currentLang];

    submitBtn.disabled  = true;
    submitBtn.innerHTML = t['form.sending'];
    statusEl.className  = 'form-status';
    statusEl.textContent = '';

    try {
        var response = await fetch('https://formspree.io/f/xrgrkpgv', {
            method:  'POST',
            body:    new FormData(form),
            headers: { 'Accept': 'application/json' }
        });

        if (response.ok) {
            statusEl.className  = 'form-status success';
            statusEl.innerHTML  = t['form.success'];
            form.reset();
            submitBtn.innerHTML = t['contact.form.send'];
        } else {
            var json = await response.json();
            var msg  = (json.errors && json.errors.map(function (err) { return err.message; }).join(', '))
                        || t['form.fallback'];
            throw new Error(msg);
        }
    } catch (err) {
        statusEl.className  = 'form-status error';
        statusEl.innerHTML  = t['form.error'] + err.message;
        submitBtn.innerHTML = t['contact.form.send'];
    } finally {
        submitBtn.disabled = false;
    }
});
